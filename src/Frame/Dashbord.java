    public void setappartmentList() {
        ArrayList<Appartment> obj = manger.getAprt();
        DefaultTableModel model = (DefaultTableModel) aprtlist.getModel();
        model.setRowCount(0);
        for (int i = 0; i < obj.size(); i++) {
            Object[] data = new Object[5];
            data[0] = obj.get(i).getUnit_Id();
            data[1] = obj.get(i).getAprtname();
            data[2] = obj.get(i).getBuilding();
            data[3] = obj.get(i).getPrice();
            if (obj.get(i).isStatus() == false) {
                data[4] = "Avilabel";
            } else {
                data[4] = "Booked";
            }

            model.addRow(data);

        }

    }

